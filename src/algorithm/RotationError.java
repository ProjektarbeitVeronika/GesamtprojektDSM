package algorithm;

public class RotationError {
	private double rotationPositionError;
	private double rotationAngleError;

	public double getRotationPositionError() {
		return rotationPositionError;
	}

	public void setRotationPositionError(double rotationPositionError) {
		this.rotationPositionError = rotationPositionError;
	}

	public double getRotationAngleError() {
		return rotationAngleError;
	}

	public void setRotationAngleError(double rotationAngleError) {
		this.rotationAngleError = rotationAngleError;
	}
}
